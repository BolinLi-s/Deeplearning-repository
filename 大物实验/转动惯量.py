import math
import numpy as np
def beta(t):
    beta = []
    for k in range(1, 5):
        beta_ = 2*math.pi*(k*t[k+4] - (k+4)*t[k]) / (t[k]*t[k+4]) / (t[k] - t[k+4])
        beta.append(beta_)
    avg = np.mean(beta)
    return beta, avg

def J(m, R, beta1, beta2):
    j = m*R*(9.8 - R*beta2) / (beta2 - beta1)
    return j

